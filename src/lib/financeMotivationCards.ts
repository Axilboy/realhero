/** Из месячной суммы в копейках — эквиваленты в рублях (равномерно по дням). */
export function passiveRubFromMonthlyMinor(monthlyMinor: number): {
  dailyRub: number;
  weeklyRub: number;
  monthlyRub: number;
} {
  const m = monthlyMinor / 100;
  return {
    dailyRub: m / 30,
    weeklyRub: (m * 7) / 30,
    monthlyRub: m,
  };
}

/** Нижняя граница ступени: 0 если нет дохода; иначе кратно step, минимум step. */
export function stepBucketRub(rub: number, step: number): number {
  if (rub <= 0) return 0;
  const b = Math.floor(rub / step) * step;
  return b === 0 ? step : b;
}

function tierIndexForDailyBucket(bucketRub: number): number {
  if (bucketRub <= 0) return 0;
  const cap = DAY_THRESHOLDS[DAY_THRESHOLDS.length - 1] ?? 5000;
  const b = Math.min(bucketRub, cap);
  let idx = 0;
  for (let i = 0; i < DAY_THRESHOLDS.length; i++) {
    if (b >= DAY_THRESHOLDS[i]!) idx = i;
  }
  return idx;
}

