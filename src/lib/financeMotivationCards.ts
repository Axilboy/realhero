export type MotivationPack = {
  dayBucketRub: number;
  weekBucketRub: number;
  monthBucketRub: number;
  day: string;
  week: string;
  month: string;
};

export type MotivationPicks = {
  day: number;
  week: number;
  month: number;
};

function pickTriple(
  tier: readonly (readonly [string, string, string])[],
  tierIndex: number,
  variant: number,
): string {
  const row = tier[tierIndex] ?? tier[tier.length - 1]!;
  const i = ((variant % 3) + 3) % 3;
  return row[i]!;
}

/**
 * Один текст на день / неделю / месяц по оценке месячного пассива (копейки).
 * Уровень текста берётся по дневной корзине (до 5000 ₽/день), чтобы три блока совпадали по смыслу.
 */
export function getMotivationFromMonthlyMinor(
  monthlyPassiveMinor: number,
  picks: MotivationPicks,
): MotivationPack | null {
  if (monthlyPassiveMinor <= 0) return null;
  const { dailyRub, weeklyRub, monthlyRub } =
    passiveRubFromMonthlyMinor(monthlyPassiveMinor);

  const dayBucketRub = stepBucketRub(dailyRub, DAY_STEP_RUB);
  const weekBucketRub = stepBucketRub(weeklyRub, WEEK_STEP_RUB);
  const monthBucketRub = stepBucketRub(monthlyRub, MONTH_STEP_RUB);

  const cap = DAY_THRESHOLDS[DAY_THRESHOLDS.length - 1] ?? 5000;
  const di = tierIndexForDailyBucket(Math.min(dayBucketRub, cap));

  return {
    dayBucketRub,
    weekBucketRub,
    monthBucketRub,
    day: pickTriple(DAY_TIERS, di, picks.day),
    week: pickTriple(WEEK_TIERS, di, picks.week),
    month: pickTriple(MONTH_TIERS, di, picks.month),
  };
}
