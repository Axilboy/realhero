export type MotivationPack = {
  dayBucketRub: number;
  weekBucketRub: number;
  monthBucketRub: number;
  day: readonly string[];
  week: readonly string[];
  month: readonly string[];
};

/** Три карточки на день / неделю / месяц по оценке месячного пассивного потока (копейки). */
export function getMotivationFromMonthlyMinor(
  monthlyPassiveMinor: number,
): MotivationPack | null {
  if (monthlyPassiveMinor <= 0) return null;
  const { dailyRub, weeklyRub, monthlyRub } =
    passiveRubFromMonthlyMinor(monthlyPassiveMinor);

  const dayBucketRub = stepBucketRub(dailyRub, DAY_STEP_RUB);
  const weekBucketRub = stepBucketRub(weeklyRub, WEEK_STEP_RUB);
  const monthBucketRub = stepBucketRub(monthlyRub, MONTH_STEP_RUB);

  const di = tierIndex(dayBucketRub, DAY_STEP_RUB, DAY_TIERS.length);
  const wi = tierIndex(weekBucketRub, WEEK_STEP_RUB, WEEK_TIERS.length);
  const mi = tierIndex(monthBucketRub, MONTH_STEP_RUB, MONTH_TIERS.length);

  return {
    dayBucketRub,
    weekBucketRub,
    monthBucketRub,
    day: DAY_TIERS[di]!,
    week: WEEK_TIERS[wi]!,
    month: MONTH_TIERS[mi]!,
  };
}
