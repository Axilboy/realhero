/**
 * Мотивационные подписи к пассивному потоку (оценка в месяц → день / неделя / месяц).
 * Шаги: день 5 ₽, неделя 25 ₽, месяц 50 ₽.
 */

export const DAY_STEP_RUB = 5;
export const WEEK_STEP_RUB = 25;
export const MONTH_STEP_RUB = 50;

/** Из месячной суммы в копейках — эквиваленты в рублях (равномерно по дням). */
export function passiveRubFromMonthlyMinor(monthlyMinor: number): {
  dailyRub: number;
  weeklyRub: number;
  monthlyRub: number;
} {
  const m = monthlyMinor / 100;
  return {
    dailyRub: m / 30,
    weeklyRub: (m * 7) / 30,
    monthlyRub: m,
  };
}

/** Нижняя граница ступени: 0 если нет дохода; иначе кратно step, минимум step. */
export function stepBucketRub(rub: number, step: number): number {
  if (rub <= 0) return 0;
  const b = Math.floor(rub / step) * step;
  return b === 0 ? step : b;
}

type Triple = readonly [string, string, string];

/** Индекс ступени 0..len-1 по величине корзины в рублях и шагу. */
function tierIndex(bucketRub: number, step: number, len: number): number {
  if (bucketRub <= 0) return 0;
  const i = Math.floor(bucketRub / step) - 1;
  return Math.max(0, Math.min(len - 1, i));
}

